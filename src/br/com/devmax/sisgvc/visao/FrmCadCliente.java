    private void novoCliente() {
        habilitaBotoesParaEdicao(true);
        limparCampos();
        operacao = 1;
    }

    private void editarCliente() {
        habilitaBotoesParaEdicao(true);
    }

    private void excluirCliente() {
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este cliente?", null, JOptionPane.OK_CANCEL_OPTION);
        System.out.println(opcao);
        if (opcao == 0) {
            ClienteDAO dao = new ClienteDAO();
            dao.excluir(clienteBean);
            reiniciaTela();
        }
    }

    private void salvar() {
        if(dadosValidos()){
            //setamos o objeto EquipeBean
        setaClienteBean();
        //Instanciamos o DAO
        ClienteDAO dao = new ClienteDAO();
        //verifica qual será a operação de peristência a ser realizada
        if (operacao == 1) {
            dao.inserir(clienteBean);
        }
        if (operacao == 2) {
            dao.alterar(clienteBean);
        }
        habilitaBotoesParaEdicao(false);
        reiniciaTela();
        }

    }

    private void habilitaBotoesParaEdicao(boolean edicao) {

        if (edicao) {
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(true);
        } else {
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btSalvar.setEnabled(false);
        }
        editarCampos(edicao);
    }

