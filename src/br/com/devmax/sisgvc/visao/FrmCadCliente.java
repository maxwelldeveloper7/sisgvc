    private void carregaCbCidades() {
        cbCidade.removeAllItems();
        cbCidade.addItem("");        
        CidadeDAO dao = new CidadeDAO();
        cidades = dao.listar();
        for (int i = 0; i < cidades.size(); i++) {
            cbCidade.addItem(cidades.get(i).getCidade());
        }
    }

    public void atualizaCbCidades() {
        cidades.clear();
        CidadeDAO dao = new CidadeDAO();
        cidades = dao.listar();
        cbCidade.setSelectedIndex(0);
        //não removemos todos os itens de uma vez pois o método cbDiscriminacaoI é acionado
        //e caso não haja pelo menos 1 item lançara uma exceção.
        for (int i = cbCidade.getItemCount() - 1; i > 0; i--) {
            if (cbCidade.getItemCount() > 0) {
                cbCidade.removeItemAt(i);
            }
        }

        for (int i = 0; i < cidades.size(); i++) {
            cbCidade.addItem(cidades.get(i).getCidade());
        }
    }

    private void setaCidadeBean() {
        CidadeBean cidade = new CidadeBean();
        for (int i = 0; i < cidades.size(); i++) {
            if(cidades.get(i).getCidade().equals(cbCidade.getSelectedItem().toString())){
                tfUf.setText(cidades.get(i).getUf());
                cidade.setId(cidades.get(i).getId());
                cidade.setCidade(cidades.get(i).getCidade());
                cidade.setUf(cidades.get(i).getUf());
                clienteBean.setCidade(cidade);
                break;
            }
        }
    }

    private boolean dadosValidos() {
        if(clienteBean.getCidade() == null){
            JOptionPane.showMessageDialog(this, "Informe a cidade");
            cbCidade.requestFocus();
            return false;
        }else{
            return true;
        }
    }
