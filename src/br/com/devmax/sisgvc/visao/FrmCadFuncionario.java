    private void tbLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {
            funcionarioBean.setId(lista.get(tbFuncionarios.getSelectedRow()).getId());
            tfNome.setText(lista.get(tbFuncionarios.getSelectedRow()).getNome());
            tfRg.setText(lista.get(tbFuncionarios.getSelectedRow()).getRg());
            ftfCpf.setText(lista.get(tbFuncionarios.getSelectedRow()).getCpf());
            ftfAdmissao.setText(lista.get(tbFuncionarios.getSelectedRow()).getDataAdmissaoStr());
            ftfDemissao.setText(lista.get(tbFuncionarios.getSelectedRow()).getDataDemissaoStr());
            tfEndereco.setText(lista.get(tbFuncionarios.getSelectedRow()).getEndereco());
            cbUf.setSelectedItem(lista.get(tbFuncionarios.getSelectedRow()).getUf());
            tfCidade.setText(lista.get(tbFuncionarios.getSelectedRow()).getCidade());
            ftfCep.setText(lista.get(tbFuncionarios.getSelectedRow()).getCep());
            ftfTelefone.setText(lista.get(tbFuncionarios.getSelectedRow()).getTelefone());
            tfContaCorrente.setText(lista.get(tbFuncionarios.getSelectedRow()).getContaCorrente());
            tfAgencia.setText(lista.get(tbFuncionarios.getSelectedRow()).getNumeroAgencia());
            tfBanco.setText(lista.get(tbFuncionarios.getSelectedRow()).getNomeBanco());
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            operacao = 2;
        } else {
            limparCampos();
            editarCampos(false);
            reiniciaBotoes();
        }
    }

    private void InicializarTabela() {

        tbFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(400);
        tbFuncionarios.setModel(tableModel);
        tbFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        lms = tbFuncionarios.getSelectionModel();
        lms.addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    tbLinhaSelecionada(tbFuncionarios);
                }
            }
        });
        atualizarDadosDaTabela();
    }

    private void atualizarDadosDaTabela() {
        FuncionarioDAO dao = new FuncionarioDAO();
        lista = dao.listar();

        if (tableModel.getRowCount() > 0) {
            limparTabela();
        }

        String[] campos = {null};


        for (int i = 0; i < lista.size(); i++) {
            tableModel.addRow(campos);
            tableModel.setValueAt(lista.get(i).getNome(), i, 0);
        }
    }

    private void limparTabela() {
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
    }
