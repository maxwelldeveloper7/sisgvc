    private void excluirFuncionario() {
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este funcionário?", null, JOptionPane.OK_CANCEL_OPTION);
        System.out.println(opcao);
        if (opcao == 0) {
            FuncionarioDAO dao = new FuncionarioDAO();
            dao.excluir(funcionarioBean);
            reiniciaTela();
        }
    }

    private void salvar() {
        //setamos o objeto fornecedorBean
        setaFornecedorBean();
        //Instanciamos o DAO
        FuncionarioDAO dao = new FuncionarioDAO();
        //verifica qual será a operação de peristência a ser realizada
        if (operacao == 1) {
            dao.inserir(funcionarioBean);
        }
        if (operacao == 2) {
            dao.alterar(funcionarioBean);
        }
        habilitaBotoesParaEdicao(false);
        reiniciaTela();
    }

    private void habilitaBotoesParaEdicao(boolean edicao) {

        if (edicao) {
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(true);
        } else {
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btSalvar.setEnabled(false);
        }
        editarCampos(edicao);
    }

    private void limparCampos() {
        tfNome.setText("");
        tfRg.setText("");
        ftfCpf.setText("");
        ftfAdmissao.setText("");
        ftfDemissao.setText("");
        tfEndereco.setText("");
        cbUf.setSelectedItem("");
        tfCidade.setText("");
        ftfCep.setText("");
        ftfTelefone.setText("");
        tfContaCorrente.setText("");
        tfAgencia.setText("");
        tfBanco.setText("");
        tfNome.requestFocus();
    }

    private void editarCampos(boolean b) {
        tfNome.setEditable(b);
        tfRg.setEditable(b);
        ftfCpf.setEditable(b);
        ftfAdmissao.setEditable(b);
        ftfDemissao.setEditable(b);
        tfEndereco.setEditable(b);
        cbUf.setEnabled(b);
        tfCidade.setEditable(b);
        ftfCep.setEditable(b);
        ftfTelefone.setEditable(b);
        tfContaCorrente.setEditable(b);
        tfAgencia.setEditable(b);
        tfBanco.setEditable(b);
        tbFuncionarios.setEnabled(!b);
        tfNome.requestFocus();
    }

    public void reiniciaTela() {
        tbFuncionarios.clearSelection();
        atualizarDadosDaTabela();
        limparCampos();
        reiniciaBotoes();
        editarCampos(false);
        operacao = 0;
    }

    private void reiniciaBotoes() {
        btNovo.setEnabled(true);
        btEditar.setEnabled(false);
        btExcluir.setEnabled(false);
        btSalvar.setEnabled(false);
    }

    private void setaFornecedorBean() {
        funcionarioBean.setNome(tfNome.getText());
        funcionarioBean.setRg(tfRg.getText());
        funcionarioBean.setCpf(ftfCpf.getText());
        funcionarioBean.setDataAdmissao(ftfAdmissao.getText());
        funcionarioBean.setDataDemissao(ftfDemissao.getText());
        funcionarioBean.setEndereco(tfEndereco.getText());
        funcionarioBean.setUf(cbUf.getSelectedItem().toString());
        funcionarioBean.setCidade(tfCidade.getText());
        funcionarioBean.setCep(ftfCep.getText());
        funcionarioBean.setTelefone(ftfTelefone.getText());
        funcionarioBean.setContaCorrente(tfContaCorrente.getText());
        funcionarioBean.setNumeroAgencia(tfAgencia.getText());
        funcionarioBean.setNomeBanco(tfBanco.getText());
    }
