    private void novoPedido() {
        habilitaBotoesParaEdicao(true);
        FrmCadCompra frmCadCompra = new FrmCadCompra(null, true, null);
        frmCadCompra.setVisible(true);
        
    }

    private void editarPedido() {
        habilitaBotoesParaEdicao(true);
        FrmCadCompra frmCadCompra = new FrmCadCompra(null, true, compraBean);
        frmCadCompra.setVisible(true);
        
    }

    private void excluirPedido() {
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir esta compra?", null, JOptionPane.OK_CANCEL_OPTION);
        if (opcao == 0) {
            CompraDAO dao = new CompraDAO();
            dao.excluir(compraBean);
            reiniciaTela();
        }
    }

    private void habilitaBotoesParaEdicao(boolean edicao) {

        if (edicao) {
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
        } else {
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
        }

    }

    public void reiniciaTela() {
        tbVendas.clearSelection();
        atualizarDadosDaTabela();
        reiniciaBotoes();       
    }

    private void reiniciaBotoes() {
        btNovo.setEnabled(true);
        btEditar.setEnabled(false);
        btExcluir.setEnabled(false);
    }    

