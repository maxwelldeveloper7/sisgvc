    private DefaultTableModel tableModel = new DefaultTableModel(null, new String[]{"Código","Nota Fiscal", "Fornecedor", "CNPJ", "Emissão", "Entrega"});
    private ListSelectionModel lms;


    private void tbLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {
            compraBean = new CompraBean();
            compraBean.setId(lista.get(tb.getSelectedRow()).getId());
            compraBean.setNtFiscal(lista.get(tb.getSelectedRow()).getNtFiscal());
            compraBean.setFornecedor(lista.get(tb.getSelectedRow()).getFornecedor());
            compraBean.setDataEmissao(lista.get(tb.getSelectedRow()).getDataEmissao());
            compraBean.setDataEntrega(lista.get(tb.getSelectedRow()).getDataEntrega());
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btNovo.setEnabled(false);
        } else {
            compraBean = null;
            reiniciaBotoes();

        }
    }

    private void inicializarTabela() {

        tbVendas.getColumnModel().getColumn(0).setPreferredWidth(50);
        tbVendas.getColumnModel().getColumn(1).setPreferredWidth(200);
        tbVendas.getColumnModel().getColumn(2).setPreferredWidth(200);
        tbVendas.getColumnModel().getColumn(3).setPreferredWidth(200);
        tbVendas.setModel(tableModel);
        tbVendas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        lms = tbVendas.getSelectionModel();
        lms.addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    tbLinhaSelecionada(tbVendas);
                }
            }
        });
        atualizarDadosDaTabela();
    }

    public void atualizarDadosDaTabela() {
        CompraDAO dao = new CompraDAO();
        lista = dao.listar();
               

        if (tableModel.getRowCount() > 0) {
            limparTabela();
        }

        String[] campos = {null,null, null, null, null, null};


        for (int i = 0; i < lista.size(); i++) {
            tableModel.addRow(campos);
            tableModel.setValueAt(lista.get(i).getId(), i, 0);
            tableModel.setValueAt(lista.get(i).getNtFiscal(), i, 1);
            tableModel.setValueAt(lista.get(i).getFornecedor().getNome(), i, 2);
            tableModel.setValueAt(lista.get(i).getFornecedor().getCnpj(), i, 3);
            tableModel.setValueAt(lista.get(i).getDataEmissaoStr(), i, 4);
            tableModel.setValueAt(lista.get(i).getDataEntregaStr(), i, 5);
        }
    }

    private void limparTabela() {
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
    }
